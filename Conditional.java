public class Conditional {
    public static void main(String[] args) {
        // exaple on how to use conditional operator with int

        int a = (7 > 3 ) ? 7 : 3;
        System.out.println(a);

        // exaple on how to use conditional operator with boolean
        String b = "hello";
        
        boolean resualt = b.equals("hello") ? true : false;
        
        System.out.println(resualt);

        // exaple on how to use conditional operator with double
        String c = "hello";
        
        double resualtc = c.equals("hello") ? 0.5 : 0.25;
        
        System.out.println(resualtc);
    }
    
}
